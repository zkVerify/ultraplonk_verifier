use clap::{Parser, Subcommand};
use std::path::PathBuf;

#[derive(Parser, Debug)]
#[command(name = "noir-cli")]
#[command(
    about = "Utility for converting proofs and verification keys generated by Barretentberg backend into zkVerify format. Compatible with Noir toolchain >= v0.31.0"
)]
#[command(version)]
pub struct Cli {
    #[command(subcommand)]
    pub command: Commands,

    /// Enable verbose output
    #[arg(long)]
    pub verbose: bool,
}

#[derive(Subcommand, Debug)]
pub enum Commands {
    /// Convert Barretenberg verification key to zkVerify format
    Key {
        /// Input file for verification key
        #[arg(long)]
        input: PathBuf,

        /// Output file for verification key [or stdout if not specified]
        #[arg(long)]
        output: Option<PathBuf>,
    },
    /// Parse proof as generated by bb tool from Noir toolchain and output files for proof and
    /// public inputs suitable to be submitted to zkVerify. Both in hex and binary format.
    ProofData {
        /// Number of public inputs supported by the circuit
        #[arg(short)]
        num_inputs: usize,

        /// Proof as generated by bb tool
        #[arg(long)]
        input_proof: PathBuf,

        /// Output file for proof data [or stdout if not specified]
        #[arg(long)]
        output_proof: Option<PathBuf>,

        /// Output file for verification key [or stdout if not specified]
        #[arg(long)]
        output_pubs: Option<PathBuf>,
    },
    /// Verify proof with key
    Verify {
        /// Proof file
        #[arg(long)]
        proof: PathBuf,

        /// Input file for public inputs
        #[arg(long)]
        pubs: PathBuf,

        /// Verification key file
        #[arg(long)]
        key: PathBuf,
    },
    /// Convert a binary file to a hex-encoded string.
    Hexdump {
        /// Input binary file
        #[arg(long)]
        input: PathBuf,

        /// Output hex-encoded file [or stdout if not specified]
        #[arg(long)]
        output: Option<PathBuf>,
    },
}
